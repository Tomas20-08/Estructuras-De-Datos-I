
package PruebaDiagnostica;

public class Ejercicio04_TrianguloNumericoDescendente_ {

    public static void main(String[] args) {  
         
        int x , y ;
        
        for (x=10 ; x>=1 ; x--){
            for (y=x ; y>=1 ; y--){
                
                System.out.print(y);
            }
            
            System.out.println();
            
        }
    }   
}
