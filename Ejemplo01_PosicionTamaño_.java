
package Array;

public class Ejemplo01_PosicionTamaño_ {

    public static void main(String[] args) {
        
        int[] num = new int[3];
        
        System.out.println("Tamaño del array: 3");
        
        num[0] = 13;
        num[1] = 1;
        num[2] = 25;
       
        System.out.println("Posicion 0: " + num[0]);
        System.out.println("Posicion 1: " + num[1]);
        System.out.println("Posicion 2: " + num[2]);
        

    }
    
}
